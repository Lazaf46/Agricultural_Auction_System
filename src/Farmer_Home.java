    private void btn_faddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_faddItemActionPerformed
        // TODO add your handling code here:
        //Show error msg Field Required
        if(txt_ownerName.getText().equals("")||txt_itemName.getText().equals("")||txt_quantity.getText().equals("")||txt_resPrice.getText().equals("")||txt_imagePath.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please Fill All The Fields to Add your Item", "Required Filed", 2);
        }else{
            //Get the typing datas to the variable        
        String ownerName = txt_ownerName.getText();
        String itemName = txt_itemName.getText();
        String quantity = txt_quantity.getText();
        String resPrice = txt_resPrice.getText();
        //Getting the img path to set in File Obj
        String imgPath = txt_imagePath.getText();
        
        
        try {
            //Creating a File Obj to find the img path
            File f = new File(imgPath);
            //Created the iS to read the imgPath and store the img in "iS" variable
            InputStream iS = new FileInputStream(f);
            Statement stmt = conn.createStatement();
            String insertQ = ("INSERT INTO faddProduct(OwnerName, ItemName, Quantity, ReservedPrice,Image) VALUES ('"+ownerName+"', '"+itemName+"', '"+quantity+"', '"+resPrice+"', '"+iS+"') ");
            stmt.execute(insertQ);
            stmt.close();
           
            
            txt_ownerName.setText("");
            txt_itemName.setText("");
            txt_quantity.setText("");
            txt_resPrice.setText("");
            txt_imagePath.setText("");
        
            
            JOptionPane.showMessageDialog(null, "Item Addedd successfully", "Add Item", 1);
            
        } catch (SQLException ex) {
            Logger.getLogger(Farmer_Home.class.getName()).log(Level.SEVERE, null, ex);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Farmer_Home.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Farmer_Home.class.getName()).log(Level.SEVERE, null, ex);
        }
             tabledata();
        }
        
        
       
        
        
        
        
    }//GEN-LAST:event_btn_faddItemActionPerformed

