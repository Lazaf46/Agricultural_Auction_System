    public void startTimer(){
        timer = new Timer(1000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                Sec--;
                if(Sec==-1){
                    timer.stop();
                    //want to adf the tabledata()
                }
                else if(Sec>=0&&Sec<10)lbl_timer.setText("00:0"+Sec);
                else lbl_timer.setText("00:"+Sec);
            }
        });
    }
    
    public void tabledata(){
         //Retreving data from db = faddProduct
        DefaultTableModel tm = (DefaultTableModel) tbl_fAddedProducts.getModel();
        
        try {
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT * from faddProduct  "/*Id, Image, OwnerName, ItemNAME, Quantity, ReservedPrice*//*ORDER BY ID DESC LIMIT 1*/);
            
            while(rs.next()){
                int id = rs.getInt("Id");
                Blob img = rs.getBlob("Image");
                String oName = rs.getString("OwnerName");
                String iName = rs.getString("ItemName");
                String quan = rs.getString("Quantity");
                String rPrice = rs.getString("ReservedPrice");
                String wName = rs.getString("Winner_Name");
                int sPrice = rs.getInt("SoldPrice");
                
                tm.addRow(new Object[]{ id,img,oName, iName, quan, rPrice, wName, sPrice });
               
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Farmer_Home.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }
    
