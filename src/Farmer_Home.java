    private void btn_imgSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_imgSelectActionPerformed
        // TODO add your handling code here:
        //pop-up a window with all files in the pc
        JFileChooser fileChooser = new JFileChooser();
        //Choose only "png", "jpg", "jpeg"
        FileNameExtensionFilter fnef = new FileNameExtensionFilter("IMAGES", "png", "jpg", "jpeg");
        fileChooser.addChoosableFileFilter(fnef);
        int openDialog = fileChooser.showOpenDialog(null);
        if(openDialog == fileChooser.APPROVE_OPTION){
            //Save the selected File in this variable
            File selectedFile = fileChooser.getSelectedFile();
            String selectedImgPath = selectedFile.getAbsolutePath();
            //Setting the path of the selected item
            txt_imagePath.setText(selectedImgPath);
            
            //Displaying Image
            ImageIcon Ii = new ImageIcon(selectedImgPath);
            //Resizing to fit the label
            Image img = Ii.getImage().getScaledInstance(lbl_displayImg.getWidth(), lbl_displayImg.getHeight(), Image.SCALE_SMOOTH);
            lbl_displayImg.setIcon(new ImageIcon(img));
        }
        
    }//GEN-LAST:event_btn_imgSelectActionPerformed

