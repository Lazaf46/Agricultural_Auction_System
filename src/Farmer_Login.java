    private void btn_fLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_fLoginActionPerformed
        // TODO add your handling code here:
        String userName = txt_fUserName.getText();
        String pass = txt_fPass.getText();
        
        try {
            Statement stmt = conn.createStatement();
            String loginQ = ("Select User_name, Password FROM sign_up WHERE User_name='"+userName+"' AND Password='"+pass+"'");
            ResultSet rs = stmt.executeQuery(loginQ);
            if(rs.next()){
                setVisible(false);
                new Farmer_Home().setVisible(true);
              
            }
            else{
                JOptionPane.showMessageDialog(null, "User_name or password is incorect", "login", 1);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Farmer_Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_fLoginActionPerformed

